package com.dc.assignement.primenumberselection.paxos.model;

public class DevideRange {
	private int start;
	private int end;
	private int randomPrimeNumber;

	public DevideRange() {

	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public int getRandomPrimeNumber() {
		return randomPrimeNumber;
	}

	public void setRandomPrimeNumber(int randomPrimeNumber) {
		this.randomPrimeNumber = randomPrimeNumber;
	}

}
